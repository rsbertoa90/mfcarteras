    {
        $image = $request->file('image');
        $path = $image->storePublicly('/images/variants');
        $path = '/storage/'.$path;
        $new = VariantImage::create([
                'variant_id' => $request->variant_id,
                'url' => $path,
                'order'=>2]);

        return $new;
    }



